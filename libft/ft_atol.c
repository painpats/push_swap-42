#include "libft.h"

long	ft_atol(const char *nptr)
{
	long	i;
	long	minus;
	long	nbr;

	i = 0;
	minus = 1;
	nbr = 0;
	while (nptr[i] == 32 || nptr[i] == '\f' || nptr[i] == '\n'
		|| nptr[i] == '\r' || nptr[i] == '\t' || nptr[i] == '\v')
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			minus = minus * -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		nbr = nbr * 10 + (nptr[i] - '0');
		i++;
	}
	return (nbr * minus);
}
